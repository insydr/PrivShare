// ============================================
// MESSAGE HANDLER
// ============================================

self.onmessage = async (event: MessageEvent<WasmWorkerMessage>) => {
    const { type, id, payload } = event.data;
    
    log(`Received message: ${type} (id: ${id})`);
    
    switch (type) {
        case 'INIT':
            await handleInit(id);
            break;
            
        case 'LOAD_IMAGE':
            await handleLoadImage(id, payload as { buffer: ArrayBuffer; fileName: string });
            break;
            
        case 'REDACT_BOX':
            await handleRedactBox(id, payload as { buffer: ArrayBuffer; box: Box });
            break;
            
        case 'REDACT_MULTIPLE':
            await handleRedactMultiple(id, payload as { buffer: ArrayBuffer; boxes: Box[] });
            break;
            
        case 'GET_HASH':
            await handleGetHash(id, payload as { buffer: ArrayBuffer; label?: string });
            break;
            
        case 'GET_IMAGE_INFO':
            await handleGetImageInfo(id, payload as { buffer: ArrayBuffer });
            break;
            
        case 'RESIZE_IMAGE':
            await handleResizeImage(id, payload as { buffer: ArrayBuffer; newWidth: number; newHeight: number });
            break;
            
        case 'TO_GRAYSCALE':
            await handleToGrayscale(id, payload as { buffer: ArrayBuffer });
            break;
            
        case 'DETECT_PII':
            await handleDetectPii(id, payload as { regionsJson: string });
            break;
            
        case 'GET_MODULE_INFO':
            await handleGetModuleInfo(id);
            break;
            
        case 'TERMINATE':
            log('Terminating worker');
            self.close();
            break;
            
        default:
            sendError(id, 'PROCESSING_ERROR', `Unknown message type: ${type}`);
    }
};

// ============================================
// ERROR HANDLING
// ============================================

self.onerror = (error: ErrorEvent) => {
    log(`Worker error: ${error.message}`, 'error');
    return false;
};

self.onunhandledrejection = (event: PromiseRejectionEvent) => {
    log(`Unhandled promise rejection: ${event.reason}`, 'error');
};

// Signal that worker is ready to receive messages
log('Worker thread started, waiting for INIT command');
